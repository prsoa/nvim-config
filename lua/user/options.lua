-- :help options
vim.opt.showtabline = 2			      -- always show tabs
vim.opt.splitbelow = true		      -- force horizontal splits to go below
vim.opt.splitright = true		      -- force vertical splits to go right
vim.opt.expandtab = true		      -- convert tabs to spaces
vim.opt.shiftwidth = 2			      -- the number of spaces inserted for each identation
vim.opt.tabstop = 2			          -- insert 2 spaces for a tab
vim.opt.number = true			        -- set numbered lines
vim.opt.termguicolors=true        -- set term gui colors
